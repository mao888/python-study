# 颜色映射：0=空地(白色), 1=草墙(绿色), 2-4=石头(灰色), 9=不生成食物(红色)
color_map = {
    0: 'white',
    1: 'green',
    2: 'gray',
    3: 'gray',
    4: 'gray',
    9: 'red'
}

# 创建颜色矩阵
color_matrix = np.array([[color_map[cell] for cell in row] for row in map_data])

# 绘制地图
fig, ax = plt.subplots(figsize=(10, 5))
for i in range(len(map_data)):
    for j in range(len(map_data[0])):
        ax.add_patch(plt.Rectangle((j, len(map_data) - i - 1), 1, 1, color=color_matrix[i][j]))

ax.set_xlim(0, len(map_data[0]))
ax.set_ylim(0, len(map_data))
ax.set_xticks([])
ax.set_yticks([])
ax.set_aspect('equal')
plt.title("Map Visualization")
